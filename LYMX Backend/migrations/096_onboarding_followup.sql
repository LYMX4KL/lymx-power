-- ─── 6. fn_mark_onboarding_booking_for_business RPC ─────────────────────────
-- Called by the book-onboarding-call.html page right after the booking insert
-- succeeds AND the URL contained ?biz=<slug>. Server-side gate prevents anon
-- from arbitrarily marking biz rows: it only succeeds if the booking's
-- business_id matches the biz looked up by slug AND the booking row was
-- created in the last 5 minutes (so anyone can use this immediately after
-- their own booking but it can't be replayed).
CREATE OR REPLACE FUNCTION public.fn_mark_onboarding_booking_for_business(
    p_booking_id uuid,
    p_biz_slug   text
) RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $mark_booking$
DECLARE
    v_bk public.onboarding_bookings%ROWTYPE;
    v_biz public.businesses%ROWTYPE;
BEGIN
    IF p_booking_id IS NULL OR p_biz_slug IS NULL THEN
        RETURN jsonb_build_object('ok', false, 'reason', 'missing_args');
    END IF;

    SELECT * INTO v_bk FROM public.onboarding_bookings WHERE id = p_booking_id;
    IF NOT FOUND THEN
        RETURN jsonb_build_object('ok', false, 'reason', 'booking_not_found');
    END IF;
    -- Freshness gate
    IF v_bk.created_at < now() - interval '5 minutes' THEN
        RETURN jsonb_build_object('ok', false, 'reason', 'booking_too_old');
    END IF;

    SELECT * INTO v_biz FROM public.businesses WHERE slug = p_biz_slug;
    IF NOT FOUND THEN
        RETURN jsonb_build_object('ok', false, 'reason', 'business_not_found');
    END IF;

    -- Link the booking → business
    UPDATE public.onboarding_bookings
       SET business_id = v_biz.id
     WHERE id = v_bk.id;

    -- Mirror to businesses
    UPDATE public.businesses
       SET onboarding_call_booked_at  = COALESCE(onboarding_call_booked_at, now()),
           onboarding_call_at         = v_bk.starts_at,
           onboarding_call_booking_id = v_bk.id
     WHERE id = v_biz.id;

    RETURN jsonb_build_object(
        'ok',          true,
        'booking_id',  v_bk.id,
        'business_id', v_biz.id,
        'slug',        v_biz.slug,
        'starts_at',   v_bk.starts_at
    );
END $mark_booking$;

GRANT EXECUTE ON FUNCTION public.fn_mark_onboarding_booking_for_business(uuid, text) TO anon, authenticated, service_role;

COMMENT ON FUNCTION public.fn_mark_onboarding_booking_for_business(uuid, text) IS
  'Anon-callable RPC: marks a freshly-created onboarding booking as belonging to a business (via slug). Guarded by a 5-minute freshness window on the booking row so replay attacks can''t link arbitrary bookings to arbitrary businesses. Called by book-onboarding-call.html?biz=<slug> immediately after the booking insert succeeds.';

-- ─── 7. Sanity output ──────────────────────────────────────────────────────
DO $sanity_096$
DECLARE
    v_biz_cols     int;
    v_book_col     boolean;
    v_view_exists  boolean;
    v_log_exists   boolean;
    v_rpc_exists   boolean;
    v_rachel_slot  int;
BEGIN
    SELECT count(*) FROM information_schema.columns
     WHERE table_schema='public' AND table_name='businesses'
       AND column_name IN ('onboarding_call_booked_at','onboarding_call_at','onboarding_call_booking_id')
    INTO v_biz_cols;

    SELECT EXISTS (
        SELECT 1 FROM information_schema.columns
         WHERE table_schema='public' AND table_name='onboarding_bookings'
           AND column_name='business_id'
    ) INTO v_book_col;

    SELECT EXISTS (
        SELECT 1 FROM information_schema.views
         WHERE table_schema='public' AND table_name='v_unbooked_approved_businesses'
    ) INTO v_view_exists;

    SELECT EXISTS (
        SELECT 1 FROM information_schema.tables
         WHERE table_schema='public' AND table_name='onboarding_followup_sends'
    ) INTO v_log_exists;

    SELECT EXISTS (
        SELECT 1 FROM pg_proc WHERE proname='fn_mark_onboarding_booking_for_business'
    ) INTO v_rpc_exists;

    SELECT max(slot_minutes) FROM public.onboarding_hosts
     WHERE active = true
    INTO v_rachel_slot;

    RAISE NOTICE 'Module 3 migration 096: biz_cols=%/3 booking_col=% view=% log=% rpc=% rachel_slot_min=%',
        v_biz_cols, v_book_col, v_view_exists, v_log_exists, v_rpc_exists, v_rachel_slot;

    IF v_biz_cols <> 3 THEN
        RAISE EXCEPTION 'Migration 096 failed: expected 3 new biz cols, got %', v_biz_cols;
    END IF;
    IF NOT v_book_col THEN
        RAISE EXCEPTION 'Migration 096 failed: onboarding_bookings.business_id did not create';
    END IF;
    IF NOT v_view_exists THEN
        RAISE EXCEPTION 'Migration 096 failed: v_unbooked_approved_businesses did not create';
    END IF;
END $sanity_096$;

COMMIT;
